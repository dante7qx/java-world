package org.java.world.dante.demo.icr;

public class IdCardVO {
	private String address;
	private String nation;
	private String sex;
	private String name;
	private String birth;
	private String idNum;
	private String validDateSt;
	private String validDateEd;
	private String authority;

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getNation() {
		return nation;
	}

	public void setNation(String nation) {
		this.nation = nation;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBirth() {
		return birth;
	}

	public void setBirth(String birth) {
		this.birth = birth;
	}

	public String getIdNum() {
		return idNum;
	}

	public void setIdNum(String idNum) {
		this.idNum = idNum;
	}

	public String getValidDateSt() {
		return validDateSt;
	}

	public void setValidDateSt(String validDateSt) {
		this.validDateSt = validDateSt;
	}

	public String getValidDateEd() {
		return validDateEd;
	}

	public void setValidDateEd(String validDateEd) {
		this.validDateEd = validDateEd;
	}

	public String getAuthority() {
		return authority;
	}

	public void setAuthority(String authority) {
		this.authority = authority;
	}

}
